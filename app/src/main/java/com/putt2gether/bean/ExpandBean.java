public class ExpandBean {

    private String hole_number;
    private String hole_score;
    private String hole_color;
    private String agg_score;
    private String agg_color;

    public String getHole_number() {
        return hole_number;
    }

    public void setHole_number(String hole_number) {
        this.hole_number = hole_number;
    }

    public String getHole_score() {
        return hole_score;
    }

    public void setHole_score(String hole_score) {
        this.hole_score = hole_score;
    }

    public String getHole_color() {
        return hole_color;
    }

    public void setHole_color(String hole_color) {
        this.hole_color = hole_color;
    }

    public String getAgg_score() {
        return agg_score;
    }

    public void setAgg_score(String agg_score) {
        this.agg_score = agg_score;
    }

    public String getAgg_color() {
        return agg_color;
    }

    public void setAgg_color(String agg_color) {
        this.agg_color = agg_color;
    }
}
